-- 날짜 절삭
SELECT DATE_FORMAT('2020-10-04 12:33:35', '%Y-%m-01');

-- 날짜 변환
SELECT DAY(CURRENT_TIMESTAMP);

-- 요일로 변환
SELECT DATE_FORMAT(CURRENT_TIMESTAMP, '%W');

-- 두 데이터를 이어 붙이기
SELECT DATE_ADD(DATE '2020-09-01', INTERVAL '03:00:00' HOUR_SECOND) AS TIMESTAMP;

SELECT DATE('2020-09-01') AS created_date;

-- 두 날짜 차이 계산
SELECT DATEDIFF('2020-06-30', '2020-05-31') AS days;

SELECT DATEDIFF('MONTH', DATE('2020-04-31'), DATE('2020-06-30')) AS months;